export type TurkeyCity = (typeof turkeyCities)[number];

const cityBySlug = new Map(turkeyCities.map((city) => [toSlug(city), city]));

export function normalizeTurkeyCity(value?: string | null) {
  const city = value?.trim();

  if (!city) {
    return null;
  }

  return cityBySlug.get(toSlug(city)) ?? null;
}

export function isTurkeyCity(value: string): value is TurkeyCity {
  return turkeyCities.includes(value as TurkeyCity);
}
